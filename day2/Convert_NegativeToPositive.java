package week1.day2;

public class Convert_NegativeToPositive {
	public static void main(String[] args) {
		int n=-29;
		int c;
		
		if(n<0)
		{
			c=(2*(-n))+n;
			
		System.out.println("The number " +n +" is converted to " +c);
		
		}
		else
		{
			System.out.println("The given number is positive");
		}
		
			
		
	}

	

}
